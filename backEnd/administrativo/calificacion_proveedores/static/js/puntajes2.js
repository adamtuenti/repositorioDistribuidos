function suma_valores(){
	var res = 0.0;
	for(i = 0; i < arguments.length; i++){
		if(arguments[i]!=""){
			res = res+parseFloat(arguments[i]);
		}
	}
	return res;
}


function puntaje_calidad_veinticinco(){
    if(document.getElementById("veinticinco_primero").checked){
        document.getElementById("id_total_prod").value = "25";
        document.getElementById("diez_primero").checked = false;
        document.getElementById("cero_primero").checked = false;

        var v1 = document.getElementById("id_total_prod").value;
		var v2 = document.getElementById("id_total_tiempo").value;
		var v3 = document.getElementById("id_total_tiempopedido").value;
		var v4 = document.getElementById("id_total_servicio").value;
		var res = suma_valores(v1,v2,v3,v4);
        document.getElementById("id_total").value = res;
        
    }
}
